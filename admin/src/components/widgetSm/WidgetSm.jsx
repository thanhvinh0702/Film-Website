import { useState, useEffect } from "react";
import "./widgetSm.css"
import { Visibility } from '@mui/icons-material';
import axios from "axios"

export default function WidgetSm() {
    const [newUsers, setNewUsers] = useState([]);

    useEffect(() => {
        const getNewUsers = async () => {
            try {
                const res = await axios.get("/api/users?new=true", {
                    headers: {
                        token: "Bearer " + JSON.parse(localStorage.getItem("user")).accessToken
                    }
                });
                setNewUsers(res.data);
                console.log(res.data);
            }
            catch (err) {
                console.log(err);
            }
        }
        getNewUsers();
    }, [])

    return (
        <div className="widgetSm">
            <span className="widgetSmTitle">New Join Members</span>
            <ul className="widgetSmList">
                {newUsers.map((user) => (
                    <li className="widgetSmListItem">
                        <img src={user.profilePic || "https://wallpapers.com/images/hd/netflix-profile-pictures-1000-x-1000-88wkdmjrorckekha.jpg"} alt="" className="widgetSmImg" />
                        <div className="widgetSmUser">
                            <span className="widgetSmUsername">{user.username}</span>
                        </div>
                        <button className="widgetSmButton"><Visibility className="widgetSmIcon" /> Display</button>
                    </li>
                ))}
            </ul>
        </div>
    )
}
